package edu.berkeley.cs169.datamodels;

public class ContactModel {
	private String name;
	private String number;

	public String getName() {
		return name;
	}

	public void setName(String newname) {
		name = newname;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String newnumber) {
		number = newnumber;
	}
}
